// Helper function to get translated text
export function t(key: string, language: 'en' | 'es' = 'en'): string {
  if (language === 'en') return key; // Return key as-is for English

  const keys = key.split('.');
  let value: unknown = translations.es;

  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = (value as Record<string, unknown>)[k];
    } else {
      return key; // Return original key if translation not found
    }
  }

  return typeof value === 'string' ? value : key;
}

// Get all translations for a section
export function getSection(section: string, language: 'en' | 'es' = 'en'): unknown {
  if (language === 'en') return null;

  const keys = section.split('.');
  let value: unknown = translations.es;

  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = (value as Record<string, unknown>)[k];
    } else {
      return null;
    }
  }

  return value;
}
